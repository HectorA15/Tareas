package appfiguras;

public class Rombo extends Figuras {

    double d1;
    double d2;
    double l;

    public Rombo(double d1, double d2, double l) {
        this.d1 = d1;
        this.d2 = d2;
        this.l = l;
    }

    public Rombo() {
    }

    public double getD1() {
        return d1;
    }

    public void setD1(double d1) {
        this.d1 = d1;
    }

    public double getD2() {
        return d2;
    }

    public void setD2(double d2) {
        this.d2 = d2;
    }

    public double getL() {
        return l;
    }

    public void setL(double l) {
        this.l = l;
    }
    
    
    
    @Override
    public double area() {
        return (d1 * d2) / 2;
    }

    public double area(double d1, double d2) {
        return (d1 * d2) / 2;
    }

    @Override
    public double perimetro() {
        return (4 * l);
    }

    public double perimetro(double L) {
        return (4 * L);
    }
}
